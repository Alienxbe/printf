#include "libft.h"

int	ft_index(const char *s, char c)
{
	int i;

	if (!s)
		return (-1);
	i = -1;
	while (s[++i])
		if (s[i] == c)
			return (i);
	return ((s[i] == c) ? i : -1);
}
